//Pedir un numero, luego otro
var numero = parseInt(prompt('Ingresar un numero'));
var otroNumero = parseInt(prompt('Ingresar otro numero'));

//Suma
alert('La suma de tus numeros es igual a ' + (numero + otroNumero));
//Resta
alert('La resta de tus numeros es igual a ' + (numero - otroNumero));
//Multiplicacion
alert('La multiplicacion de tus numeros es igual a ' + (numero * otroNumero));
//Division
alert('La division de tus numeros es igual a ' + (numero / otroNumero));